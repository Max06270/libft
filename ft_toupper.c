#include "libft.h"

int		ft_toupper(int c)
{
	if (c >= 97 && c <= 122)
		c = c - 32;
	return (c);
}

int		main()
{
	int		i;
	i = -2;
	while (i < 130)
	{
		printf("%c %c %c\n", i, toupper(i), ft_toupper(i));
		i++;
	}
	return (0);
}
